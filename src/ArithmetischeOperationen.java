public class ArithmetischeOperationen {
    public static void main(String[] args) {
        int a = 10;
        int b = 6;
        int div = a/b;

        System.out.println(a + " + " + b + " = " + (a+b));
        System.out.println(a + " - " + b + " = " + (a-b));
        System.out.println(a + " * " + b + " = " + (a*b));
        System.out.println(a + " / " + b + " = " + div);
        System.out.println(a + " % " + b + " = " + (a%b));
        //kann auch f√ºr Addition, Division etc. extra eine Variable vergeben




    }
}
